#pragma once
#include <cstdint>
#include <string>

// Опрос игрового геймпада и преобразование его состояния в значение джойстика БК-0010
// на параллельном порту 0177714 (активный уровень высокий, нажато = 1).
//
// Реализация — БЕЗ внешних библиотек: напрямую через ядерный joystick-интерфейс Linux
// (/dev/input/js0, linux/joystick.h). Qt6 своего модуля геймпада не имеет (QtGamepad
// был только в Qt5). На не-Linux poll() просто возвращает 0.
//
// У БК исторически несколько несовместимых раскладок джойстика — выбирается
// setStandard():
//   «Стандарт» (по умолч.; распайка «Джойвокс»/эмулятор gid — документированная):
//       ВВЕРХ=001, ВПРАВО=002, ВНИЗ=004, ВЛЕВО=010; огонь1=040, огонь2=0200,
//       огонь3=0100, огонь4=0400, кнопка5=020;
//   «Break House» (BKBTL): кнопки1-4 = 001/002/004/010, ВЛЕВО=020, ВНИЗ=040,
//       ВПРАВО=0100, ВВЕРХ=0200;
//   «SWCorp» (BKBTL): кнопки1-4 = 001/002/004/010, ВПРАВО=020, ВНИЗ=040,
//       ВЛЕВО=01000, ВВЕРХ=02000 (ВЛЕВО/ВВЕРХ в старшем байте — читается словом);
//   «Клад-2» (определено реверсом самой игры): ВПРАВО=001, ВЛЕВО=002, ВВЕРХ=004,
//       ВНИЗ=010; стрельба влево/вправо = 020/040.
//
// Направления — с крестовины/стика (оси 0/1, при наличии — хат 6/7), кнопки — «огонь».
// Поддерживается горячее подключение (устройство переоткрывается на лету).
class Gamepad {
public:
    enum class Standard { Standard, BreakHouse, SWCorp, Klad2 };

    Gamepad();
    ~Gamepad();
    Gamepad(const Gamepad&) = delete;
    Gamepad& operator=(const Gamepad&) = delete;

    void setStandard(Standard s) { standard_ = s; }
    Standard standard() const { return standard_; }

    // Считать события устройства и вернуть текущее значение джойстика БК (16 бит).
    uint16_t poll();

    bool connected() const { return fd_ >= 0; }
    const std::string& name() const { return name_; }  // имя контроллера или пусто

private:
    void openDevice();
    void closeDevice();

    int         fd_ = -1;         // дескриптор /dev/input/jsN, -1 если не подключён
    std::string name_;
    Standard    standard_ = Standard::Standard;
    int16_t     axis_[16] = {};   // последнее значение осей (-32767..32767)
    uint8_t     button_[32] = {}; // состояние кнопок (0/1)
};
